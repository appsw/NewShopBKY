public class Address {
    int id;
    String name;
    String number_phone;
    String add_deils;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNumber_phone() {
        return number_phone;
    }

    public void setNumber_phone(String number_phone) {
        this.number_phone = number_phone;
    }

    public String getAdd_deils() {
        return add_deils;
    }

    public void setAdd_deils(String add_deils) {
        this.add_deils = add_deils;
    }
}
